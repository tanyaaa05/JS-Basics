let x = 5;
console.log("Original value of x:", x);

x += 3; // Equivalent to x = x + 3
console.log("Value of x after addition assignment (x += 3):", x);

x -= 2; // Equivalent to x = x - 2
console.log("Value of x after subtraction assignment (x -= 2):", x);

x *= 4; // Equivalent to x = x * 4
console.log("Value of x after multiplication assignment (x *= 4):", x);

x /= 2; // Equivalent to x = x / 2
console.log("Value of x after division assignment (x /= 2):", x);

x %= 3; // Equivalent to x = x % 3
console.log("Value of x after modulus assignment (x %= 3):", x);

x **= 2; // Equivalent to x = x ** 2
console.log("Value of x after exponentiation assignment (x **= 2):", x);


